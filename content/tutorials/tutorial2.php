        <h1>Python Variables and Types</h1>
        <p>Welcome to the second tutorial in our Python series. In this tutorial, we will explore variables and the different types of data they can hold in Python.</p>

        <h2>What is a Variable?</h2>
        <p>A variable in Python is like a container that holds data. It's a way to store information that can be used and manipulated in your program. In Python, variables are created when you assign a value to them:</p>
        <pre><code>x = 5
y = "Hello, World!"</code></pre>

        <h2>Data Types in Python</h2>
        <p>Python has various data types including integers, float (decimal numbers), strings, and more. Let's explore these:</p>

        <h3>Integers</h3>
        <p>An integer is a whole number, positive or negative, without decimals. For example:</p>
        <pre><code>age = 25</code></pre>

        <h3>Float</h3>
        <p>Float, or "floating point number", is a number with a decimal point. For example:</p>
        <pre><code>height = 5.9</code></pre>

        <h3>Strings</h3>
        <p>Strings in Python are used to store text. A string is a sequence of characters enclosed in quotes. For example:</p>
        <pre><code>name = "John Doe"</code></pre>

        <h2>Dynamic Typing</h2>
        <p>Python is dynamically typed, which means you don't have to declare the type of variable when you create one. The interpreter infers the type based on the value assigned to the variable.</p>

